export function t(key, vars = {}) {
    const lang = state.language || "fr";
    let text = key;
    if (DICTIONARY[lang] && DICTIONARY[lang][key]) {
        text = DICTIONARY[lang][key];
    } else if (DICTIONARY["fr"] && DICTIONARY["fr"][key]) {
        text = DICTIONARY["fr"][key];
    }
    
    // Replace variables (e.g. {count} or {temp})
    Object.keys(vars).forEach(vKey => {
        text = text.replace(`{${vKey}}`, vars[vKey]);
    });
    
    return text;
}

